    private void BentrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BentrarActionPerformed
        Point p = this.getLocation();
        Login login=this;
       new Thread(){
            @Override
            public void run(){
                try{
                    for(int i =0;i<4;i++){
                        login.setLocation(p.x-10, p.y);
                      sleep(20);
                        login.setLocation(p.x+10, p.y);
                        sleep(20);
                   }
                    login.setLocation(p.x, p.y);
                }catch(InterruptedException ex){
                    Logger.getLogger(Login.class.getName()).log(Level.SEVERE,null,ex);
                }
          }
       }.start();
       
       if(Jnome.getText().equals("admin")&&Jsenha.getText().equals("admin")){
           Carregar carregar = new Carregar();
           
           carregar.setVisible(true);
           
           dispose();
           
       }else{
           
           JOptionPane.showMessageDialog(null,"Usu√°rio ou senha inv√°lido");
       }
        
    }//GEN-LAST:event_BentrarActionPerformed

